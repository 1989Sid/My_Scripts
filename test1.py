list1=[2,1,3,1]
list2=["Apple","Kiwi","Orage","Coconut"]
#append sort
list1.sort()
print (list1)
list2.sort()
print (list2)
#append list
list1.append(6)
list2.append("Papaya")
print (list1)
print (list2)
#Reverse list
list1.sort(reverse=True)
print (list1)
list2.sort(reverse=True)
print (list2)
#Mirror list
list1.reverse()
list2.reverse()
print (list1)
print (list2)
#Insert list
list1.insert(2,5)
print (list1)
print (list2)